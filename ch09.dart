void main() {
  // Single line string datatype

  String FirstName = "Dev";
  String LastName = "Tajpuriya";

  print("My name is $FirstName $LastName\n\n");

  //Multi-line string datatype
  String Multiline = '''
Dear Friend,
            Hi, How are you?
  ''';

  print(Multiline);

  // You can also use this type of quotation """ """
  String otherMultiline = """
Dear Friend,
            Hi, How are you?
  """;

  print(otherMultiline);
}
